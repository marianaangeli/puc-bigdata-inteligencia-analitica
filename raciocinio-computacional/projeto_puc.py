# Módulo Turmas
def incluir_turma():
    codigo = int(input("Digite o código da turma: "))
    if verificar_codigo_existe(codigo, turmas):
        print("Código da turma já existe.")
    else:
        professor_codigo = int(input("Digite o código do professor: "))
        disciplina_codigo = int(input("Digite o código da disciplina: "))
        turmas.append({"codigo": codigo, "professor_codigo": professor_codigo, "disciplina_codigo": disciplina_codigo})
        print(f"Turma com código {codigo} incluída com sucesso!")

def listar_turmas():
    listar_dados(turmas)

def excluir_turma():
    try:
        codigo = int(input("Digite o código da turma que deseja excluir: "))
        excluir_dados(codigo, turmas)
    except ValueError:
        print("Código inválido. Deve ser um número inteiro.")

# Módulo Matrículas
def incluir_matricula():
    codigo_turma = int(input("Digite o código da turma: "))
    codigo_estudante = int(input("Digite o código do estudante: "))
    if verificar_codigo_existe(codigo_turma, turmas) and verificar_codigo_existe(codigo_estudante, estudantes):
        matriculas.append({"codigo_turma": codigo_turma, "codigo_estudante": codigo_estudante})
        print(f"Matrícula na turma {codigo_turma} para o estudante {codigo_estudante} realizada com sucesso!")
    else:
        print("Turma ou estudante não encontrados.")

def listar_matriculas():
    listar_dados(matriculas)

def excluir_matricula():
    try:
        codigo_turma = int(input("Digite o código da turma da matrícula que deseja excluir: "))
        codigo_estudante = int(input("Digite o código do estudante da matrícula que deseja excluir: "))
        for mat in matriculas:
            if mat["codigo_turma"] == codigo_turma and mat["codigo_estudante"] == codigo_estudante:
                matriculas.remove(mat)
                print(f"Matrícula na turma {codigo_turma} para o estudante {codigo_estudante} excluída com sucesso!")
                return
        print("Matrícula não encontrada.")
    except ValueError:
        print("Código inválido. Deve ser um número inteiro.")

