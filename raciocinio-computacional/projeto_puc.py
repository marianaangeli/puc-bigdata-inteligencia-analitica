# Estruturas de dados para armazenar os registros
estudantes = []
professores = []
disciplinas = []
turmas = []
matriculas = []

# Função para incluir dados genéricos
def incluir_dados(nome, lista, dados_adicionais=None):
    try:
        codigo = int(input(f"Digite o código do {nome}: "))
        nome = input(f"Digite o nome do {nome}: ").strip()

        if nome:
            if dados_adicionais:
                lista.append({"codigo": codigo, "nome": nome, **dados_adicionais})
            else:
                lista.append({"codigo": codigo, "nome": nome})
            print(f"{nome} incluído com sucesso!")
        else:
            print(f"Nome inválido. Nenhum dado foi incluído.")
    except ValueError:
        print("Código inválido. Deve ser um número inteiro.")

# Função para listar dados genéricos
def listar_dados(lista):
    if lista:
        for item in lista:
            print(f"Código: {item['codigo']}, Nome: {item['nome']}")
    else:
        print("Não há dados cadastrados.")

# Função para excluir dados genéricos
def excluir_dados(codigo, lista):
    for item in lista:
        if item["codigo"] == codigo:
            lista.remove(item)
            print(f"Registro com código {codigo} removido com sucesso!")
            return
    print("Registro não encontrado.")

# Função para atualizar dados genéricos
def atualizar_dados(codigo, lista):
    for item in lista:
        if item["codigo"] == codigo:
            novo_codigo = int(input("Novo código: "))
            novo_nome = input("Novo nome: ").strip()
            item["codigo"] = novo_codigo
            item["nome"] = novo_nome
            print(f"Registro com código {codigo} atualizado com sucesso!")
            return
    print("Registro não encontrado.")

# Função para verificar se o código já existe
def verificar_codigo_existe(codigo, lista):
    for item in lista:
        if item["codigo"] == codigo:
            return True
    return False

# Módulo Estudantes
def incluir_estudante():
    incluir_dados("estudante", estudantes)

def listar_estudantes():
    listar_dados(estudantes)

def excluir_estudante():
    try:
        codigo = int(input("Digite o código do estudante que deseja excluir: "))
        excluir_dados(codigo, estudantes)
    except ValueError:
        print("Código inválido. Deve ser um número inteiro.")

# Módulo Professores
